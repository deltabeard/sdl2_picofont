uint8_t reverse(uint8_t in)
{
	uint8_t out = 0;
	unsigned loc = 7;

	for(unsigned i = 0; i < 8; i++)
	{
		unsigned bit = (in >> i) & 0b1;
		out |= bit << loc;
		loc--;
	}

	return out;
}

